#import "UIColor+SmokeFree.h"

@implementation UIColor (SmokeFree)

+ (UIColor*)smokeFreeBlue;
{
    return [UIColor colorWithRed:0.00f green:0.68f blue:0.94f alpha:1.00f];
}

+ (UIColor*)smokeFreeGreen;
{
    return [UIColor colorWithRed:0.55f green:0.78f blue:0.25f alpha:1.00f];
}

+ (UIColor*)smokeFreeRed;
{
    return [UIColor colorWithRed:0.94f green:0.13f blue:0.00f alpha:1.00f];
}

@end
